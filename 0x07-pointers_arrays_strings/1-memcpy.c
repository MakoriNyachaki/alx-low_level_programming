#include "main.h"
/**
 * _memcpy - Copies memory area
 * @dest: pointer the memory area
 * @src: input source
 * @n: memory size to copy to
 * Return: dest
 */

char *_memcpy(char *dest, char *src, unsigned int n)
{
	char *a = dest;
	char *b = src;
	int m;

	m = 0;

	while (m < n)
	{
		a = b;
	}
	return (dest);
}
